    public String llenarComboBoxSucursal()
    {
       mListaSucursales = csSucursal.getListaSucursales();
       Iterator iterator = mListaSucursales.iterator();
       while(iterator.hasNext())
       {
           Sucursal sucursal = (Sucursal)iterator.next();
           cbSucursales.addItem(sucursal);
       }
       String nombreSucursal  =  cbSucursales.getSelectedItem().toString();
       String query = "SELECT idSucursal FROM sucursal WHERE nombre Sucursal ="+ nombreSucursal + ";";
       return query;
       
    }
