    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name = txtName.getText();
        String lasName = txtLastName.getText();
        String docTipe =  cbDocument.getSelectedItem().toString();
        String documentNumber = txtDocument.getText();
        String email = txtEmail.getText();
        String queryIdSucursal =  llenarComboBoxSucursal();
        if(name.isEmpty() || lasName.isEmpty() || docTipe.isEmpty() || documentNumber.isEmpty() || email.isEmpty() )
        {
            JOptionPane.showMessageDialog(this, "hay Campos Requeridos sin digilenciar", "Formulario Incompleto", JOptionPane.WARNING_MESSAGE);
        } else 
        {
            try 
            {
                connection = conexion.getConnection();
                st = connection.createStatement();
                rs = st.executeQuery(queryIdSucursal);
                while (rs.next())
                {
                    int idSucursal = rs.getInt("idSucursal");
                    String query = "INSERT INTO empleados (nombreEmp , apellidos , tipoDocumento , documento ,correo,FK_idSucursal) VALUES  ('" + name + "', '" + lasName + "','" + docTipe + "',' " + documentNumber + "','" + email + "','" + idSucursal + "');";
                    try 
                    {
                        st.executeUpdate(query);
                        JOptionPane.showMessageDialog(this, "Registro Exitoso");
                    } 
                    catch (SQLException e)
                    {
                        JOptionPane.showMessageDialog(this, "error No se pudo crear el usuario ", "registro error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            catch (SQLException e)
            {
                System.out.println(e);
            }

            this.dispose();
        }

    }//GEN-LAST:event_btnSaveActionPerformed

